package com.storyanvil.cogwheel.util;

public class DoubleValue<A,B> {
    private A a;
    private B b;

    public DoubleValue(A a, B b) {
        this.a = a;
        this.b = b;
    }

    public DoubleValue() {
        a = null;
        b = null;
    }

    public A getA() {
        return a;
    }

    public void setA(A a) {
        this.a = a;
    }

    public B getB() {
        return b;
    }

    public void setB(B b) {
        this.b = b;
    }
}
