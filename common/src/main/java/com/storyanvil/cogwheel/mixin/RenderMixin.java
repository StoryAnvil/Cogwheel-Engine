package com.storyanvil.cogwheel.mixin;

import com.storyanvil.cogwheel.client.CutSceneManagement;
import net.minecraft.client.render.Camera;
import net.minecraft.client.render.GameRenderer;
import org.joml.Matrix4f;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(GameRenderer.class)
public class RenderMixin {
    @Inject(method = "renderHand", at = @At("HEAD"), cancellable = true)
    public void renderItemInHand(Camera camera, float tickDelta, Matrix4f matrix4f, CallbackInfo ci) {
        if (!CutSceneManagement.isRenderHand()) {
            ci.cancel();
        }
    }
}
